package com.syq.exception;



public class FieldServiceException extends SYQException {


  private static final long serialVersionUID = 7679417944356716113L;

  public FieldServiceException() {
    super();
  }

  public FieldServiceException(String message, Throwable cause) {
    super(message, cause);
  }

  public FieldServiceException(String message) {
    super(message);
  }

  public FieldServiceException(Throwable cause) {
    super(cause);
  }



}
